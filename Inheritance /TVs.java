package Inheritance;

public class TVs { // 부모 클래스
	
	public String color; // 색상
	public boolean power; // 전원
	public int channel;  // 채널
	
	public void power() {
		power = !power;
	}
	
	public void channelUp() {
		channel++;
	}
	public void channelDown() {
		channel--;
	}

	// 이걸 상속해서 무슨무슨 티비는 이런게 있고 저런게 있다 ...
	// TV에는 전원도 있고 색깔도 있고 채널 있는거는 똑같은데 이제 기능들이 다른 것과 같음
}
